#include <stdio.h>
/* 
Exercicio 01: Faça Um programa que exiba Ola, mundo !
- O Exercicio nos força a compreender o corpo e a sintaxe basica da linguagem
- o 'main' é o ponto de execução inicial do programa, onde o void indica que ele não 
precisa de parametros de entrada, o corpo do bloco main é limitado por "{}", que indica
o inicio e o fim do bloco de código em execução
- todo bloco de código deve ser declarado com um tipo de retorno, o bloco main deve retornar zero
se sua execução foi bem sucedida, caso contrario um valor inteiro que representa o erro.
*/
int main(void){
    printf("\nOla, Mundo !\n");
    return 0;
}